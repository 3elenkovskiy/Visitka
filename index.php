Стеклодув набирает на кончик стеклодувной трубки расплавленное стекло, выдувает пузырь и начинает формовать его, постоянно вращая трубочку и формует стекло в деревянных или металлических формах.
Свободное выдувание заключается в свободном формовании изделия.
Стеклянные предметы, выполненные путём свободного выдувания, называют также гутным стеклом (иногда называют гутенским, от немецкого hutte — гута, стеклодувная мастерская).
